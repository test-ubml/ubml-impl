/**
 * The Summary of EApi/EApiOperation/EApiParameter etc.
 *
 * @Author: Fynn Qi
 * @Date: 2020/8/26 14:40
 * @Version: V1.0
 */
public class Summary {

    private String id;

    private String code;

    private String name;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

}
